#include"Rumus.h"
#include<math.h>
int tambah(int x, int y)
{
	return (x + y);
}
int kurang (int w, int r)
{
	return (w - r);
}
int kali(int a, int b)
{
	return (a * b);
}
int bagi(int d, int e)
{
	return (d / e);
}
int pangkat(int i, int j)
{
	return pow(i,j);
}
int akar(int o)
{
	return sqrt(o);
}
int sisa(int f, int g)
{
	return fmod(f,g);
}
